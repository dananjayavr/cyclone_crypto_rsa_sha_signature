int main(int argc, char* argv[]) {
	error_t error;
    uint8_t digest[64];
    //uint8_t signature[512];
    size_t signatureLength;
    RsaPublicKey publicKey;
    RsaPrivateKey privateKey;

    error = NO_ERROR;

    //Initialize RSA public and private keys
    rsaInitPublicKey(&publicKey);
    rsaInitPrivateKey(&privateKey);

    //Read RSA public key
    mpiReadRaw(&publicKey.n, modulus, 256);
    mpiReadRaw(&publicKey.e, publicExponent, 3);
    //Read RSA private key
    mpiReadRaw(&privateKey.n, modulus, 256);
    mpiReadRaw(&privateKey.e, publicExponent, 3);
    mpiReadRaw(&privateKey.d, privateExponent, 256);

    mpiReadRaw(&privateKey.p, prime1, 128);
    mpiReadRaw(&privateKey.q, prime2, 128);
    mpiReadRaw(&privateKey.dp, primeExponent1, 128);
    mpiReadRaw(&privateKey.dq, primeExponent2, 128);
    mpiReadRaw(&privateKey.qinv, coefficient, 128);

    // Create a key-pair RSA 2048 (for the moment, use OpenSSL)

    // Sign the image file (i.e. image.bin) using the previously generated private key
    error = rsassaPkcs1v15Sign(&privateKey, SHA256_HASH_ALGO,
                               digest, signature, &signatureLength);

    if(error)
        printf("Could not sign the image\n");

    // Verify the image file
    // TRUE if verified, FALSE if not.
    //RSA PKCS #1 v1.5 signature verification
    error = rsassaPkcs1v15Verify(&publicKey, SHA256_HASH_ALGO,
                                 digest, signature, 256);

    if(error) {
        printf("Could not verify the image\n");
    }

    printf("Done.\n");


    //Release previously allocated resources
    rsaFreePublicKey(&publicKey);
    rsaFreePrivateKey(&privateKey);
	return 0;
}
